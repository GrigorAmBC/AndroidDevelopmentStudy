package com.example.flea;

public class AudioTrack {
    public String data;
    public String displayName;
    public String title;
    public String _ID;

    public AudioTrack(String data, String displayName , String title, String _ID) {
        this.data = data;
        this.displayName = displayName;
        this.title = title;
        this._ID = _ID;
    }
}